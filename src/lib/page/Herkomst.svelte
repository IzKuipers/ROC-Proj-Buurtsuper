<h1 class="title">Herkomst</h1>
<p>
  <img src={koeien} alt="" />

  Slagerij Balkenbrij is opgericht door mijn grootvader, Tjidde Wiedema in 1934.
  De koeien liepen in die tijd gewoon nog op een weiland achter de slagerij en
  mijn grootvader slachtte er af en toe een om het vlees te verkopen. Ook stond
  er achter de slagerij een stal met varkens. Die kregen het slachtafval te eten
  en verder alles wat door de groenteboer niet verkocht werd. Daar kreeg de
  groenteboer natuurlijk een mooi stukje vlees voor terug. Ook alles wat van
  tafel overbleef ging naar de varkens. In die tijd was er nog geen
  vuilophaaldienst en eigenlijk was die ook niet nodig!
</p>
<p>
  In 1977 nam mijn vader het bedrijf over. Het weiland achter de slagerij werd
  volgebouwd met nieuwe woningen en ook de varkensstal moest weg in verband met
  de stankoverlast in de nieuwe wijk. Het vlees kwam niet langer rechtstreeks
  van de boer, maar mijn vader liet het komen van het slachthuis. Met de nieuwe
  wijk erbij draaide de winkel goed en hebben mijn vader en moeder de slagerij
  uitgebouwd tot een modern bedrijf.
</p>
<p>
  <img src={tjidde} alt="" class="tjidde" />
  Ook ik ben het slagersvak ingestapt en heb ongeveer 10 jaar met mijn vader samen
  de slagerij draaiende gehouden. Inmiddels was er een supermarkt in het dorp gekomen,
  waar we veel klanten aan verloren. Toen mijn vader vijf jaar geleden met pensioen
  ging, was er van het bedrijf niet veel meer over. Samen met mijn vrouw besloot
  ik het over een andere boeg te gooien en terug te keren naar de roots van Tjidde,
  mijn overgrootvader. We vonden in Balkendam een nieuw pand. Ook kunnen we in Balkendam
  weer de koeien bij de boer halen en met vergunning zelf slachten. Achter ons huis
  hebben we een schuur met 35 varkens en de groenteboer levert bij ons weer alles
  af wat hij niet kan verkopen. Ik denk dat grootvader Tjidde wel trots op ons zou
  zijn!!
</p>

